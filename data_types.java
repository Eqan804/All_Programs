public class data_types {
    public static void main(String[] args) {
        System.out.println("Data types");
        
        /*  Data types to two types 
        1. Primitive data types
        primitive - byte (1 Byte), short (2 byte), int (4 Byte), long (8 Byte), float (4 Byte)
        double (8 byte), boolean (1 byte), char (2 byte).
        2. Non primitive or Reference Data types - 
        */
        System.out.println("Primitive data types");

        // Byte
        System.out.println("This is byte value");
        // Byte max value 127 but you can max value break to error
        byte u = 56;
        System.out.println(u);

        // Double 
        System.out.println("This is double value");
        double d = 232.22222314d;
        System.out.println(d);

        // char
        System.out.println("This is char value ");
        char grade = 'F';
        System.out.println(grade);

        // Non primitive data types
        //non primitive data types programer make but primitive data types programer not make
        System.out.println("Non primitive data types");
    }
}
